    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        conn = MySqlConnect.ConnectDB();
        
        DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
        tblModel.setRowCount(0);
        String value = jComboBox1.getSelectedItem().toString();
        
        try {
            pst = conn.prepareStatement("SELECT * FROM log_table WHERE table_name LIKE ?");
            pst.setString(1, value);
            rs = pst.executeQuery();
            while (rs.next()) {
                String name = rs.getString("action_name");
                String time = rs.getString("action_time");
                String result = rs.getString("result");
                String action = rs.getString("action");
                
                String dbData[] = {name, time, result, action};
                tblModel.addRow(dbData);
            }
        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
    }//GEN-LAST:event_jComboBox1ActionPerformed


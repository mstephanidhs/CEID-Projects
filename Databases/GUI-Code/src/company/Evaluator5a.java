    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        conn = MySqlConnect.ConnectDB();
        
        int ide = 0;
        String g1 = jTextField1.getText();
        int grade1 = Integer.parseInt(g1);
        String ename = jTextField4.getText();
        String jid = jTextField5.getText();
        int id = Integer.parseInt(jid);
        String str = getUser();
        String afm = "";
        String mysql = "UPDATE evaluation_grades SET phase1 = ? WHERE job_ID = ? AND employee_usr LIKE ?";
        
        try {
            pst = conn.prepareStatement("SELECT AFM, IDeval FROM evaluator WHERE eval_usr LIKE ?");
            pst.setString(1, str);
            rs = pst.executeQuery();
            while(rs.next()) {
                afm = rs.getString("AFM");
                ide = rs.getInt("IDeval");
            }
            
            pst = conn.prepareStatement("SELECT * FROM evaluation_grades WHERE employee_usr LIKE ? AND job_ID = ? AND eval_usr LIKE ? AND AFM LIKE ?");
            pst.setString(1, ename);
            pst.setInt(2, id);
            pst.setString(3, str);
            pst.setString(4, afm);
            rs = pst.executeQuery();
            if (rs.next()) {
                pst = conn.prepareStatement(mysql);
                pst.setInt(1, grade1);
                pst.setInt(2, id);
                pst.setString(3, ename);
                int i = pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "The update was successfull");
                jTextField1.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jButton1ActionPerformed(evt);
                CallableStatement cstmt = conn.prepareCall("{call evaluationComplete(?, ?)}");
                cstmt.setInt(1, id);
                cstmt.setInt(2, ide);
                cstmt.executeUpdate();
            }
            else {
                JOptionPane.showMessageDialog(null, "Check the fields you've entered");
            }
        }   
        
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        conn = MySqlConnect.ConnectDB();
        DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
        tblModel.setRowCount(0);
        
        String str = getUser();
        String afm = "";
        int eid = 0;
        String mysql1 = "SELECT AFM, IDeval FROM evaluator WHERE eval_usr LIKE ?";
        
        try {
            pst = conn.prepareStatement(mysql1);
            pst.setString(1, str);
            rs = pst.executeQuery();
            if (rs.next()) {
                afm = rs.getString("AFM");
                eid = rs.getInt("IDeval");
            }           
            
            pst = conn.prepareStatement("SELECT * FROM evaluation_grades WHERE IDeval = ? AND AFM LIKE ?");
            pst.setInt(1, eid);
            pst.setString(2, afm);
            rs = pst.executeQuery();
            while(rs.next()) {
                String mname = rs.getString("managerUsername");
                int jid = rs.getInt("job_ID");
                String id = String.valueOf(jid);
                String ename = rs.getString("employee_usr");
                int g1 = rs.getInt("phase1");
                String grade1 = String.valueOf(g1);
                int g2 = rs.getInt("phase2");
                String grade2 = String.valueOf(g2);
                int g3 = rs.getInt("phase3");
                String grade3 = String.valueOf(g3);
                String report = rs.getString("report_manager");
                
                String dbData[] = {mname, id, ename, grade1, grade2, grade3, report};
                tblModel.addRow(dbData);
                
            }
        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

